package week1.day2;

public class Calculator {

	public int sumOfThreeNumbers (int num1, int num2, int num3) {
		int sum = num1+num2+num3;
		return sum;
	}
	
	public int subOfTwoNumbers (int num1, int num2) {
		int sub = num1-num2;
		return sub;
	}
	public double productOfTwoNumbers (double num1, double num2) {
		double product = num1*num2;
		return product;
	}
	public float divOfTwoNumbers (float num1, float num2) {
		float division = num1/num2;
		return division;
	}
	
	}

